
public class UserType {
	String name;
	String book1;
	String book2;
	
	public UserType(String text1, String text2, String text3) {
		this.name = text1;
		this.book1 = text2;
		this.book2 = text3;
		
		
	}
	public String getName() {
		
		return this.name;
	}
	public String getBook1() {
		return this.book1;
		
	}
	public String getBook2() {
		return this.book2;
	}
	
}
